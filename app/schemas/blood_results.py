    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç sii_conclusion_levels —Å–æ —Å–ª—É—á–∞–π–Ω—ã–º–∏ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏—è–º–∏ –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —É—Ä–æ–≤–Ω—è
    """
